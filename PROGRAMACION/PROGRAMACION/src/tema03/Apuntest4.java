public class Apuntest4 {

	public static void main(String[] args) {
		// Split separa en cadenas y los agrupa/devuelve un array con las posiciones que tu le indiques
		String cad = "10/12/2024";
		String arr [] = cad.split("/");
		
		for (String x : arr) {
			//System.out.print(x + " "); 
			System.out.print(x + "-");
		}
		System.out.println();
		  // Cadena con una fecha en formato "día/mes/año"
	    String ca = "10/12/2024";
	    
	    // Utilizamos split para dividir la cadena en un array de subcadenas, usando "/" como separador
	    String ar[] = ca.split("/");
	    
	    // Recorremos el array e imprimimos cada elemento seguido de un "-"
	    for (int i = 0; i < ar.length; i++) {
	        System.out.print(ar[i]);
	        
	        // Solo añade "-" si no es el último elemento
	        if (i < ar.length - 1) {
	            System.out.print("-");
	        }
	    }
	}
}